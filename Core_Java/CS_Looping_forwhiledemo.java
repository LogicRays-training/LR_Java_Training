package fundamentals;

public class CS_Looping_forwhiledemo {

	public static void main(String[] args) {
		int i;
		System.out.println("\n for loop");
		for(i=1;i<=10;i++)
		{
			System.out.println("\n i:"+i);
		}
		
		System.out.println("\n\n while loop");
		i=1;
		while(i<=10)
		{
			System.out.println("\n i:"+i);
			i++;
		}

	}

}
